class DataTypes{
    public static void main(String args[])
	{
		
	  byte sum;
	  sum = 5+5;
	  System.out.println("sum:" + sum);
	  
	  int mul = 777 * 888;
	  System.out.println("mul:" + mul); 
	   
	  long m=1000000l, n=1000l;
	  long r=m/n;
	  System.out.println("div 2 nums:" + r);
	  
	  float a=2.55f,b=1.45f;
	  float c = a+b;
	  System.out.println("sum of two float num:" + c);
	  
	  double N1=7.555555d, N2=3.45555d;
	  double N3=N1-N2;
	  System.out.println("subtraction:" + N3);
	  
	  char char1 ='A', char2 ='B';
	  System.out.println("the ASCII value of char1:" + char1);
	  System.out.println("the ASCII value of char2:" + char2);
	  
	}

}
